DELIMITER //
CREATE TRIGGER log_atualizacao_processo
AFTER UPDATE ON processo
FOR EACH ROW
BEGIN
    IF OLD.deliberacao <> NEW.deliberacao THEN
        INSERT INTO log_processo (processo_id, campo_alterado, valor_antigo, valor_novo)
        VALUES (OLD.id, 'deliberacao', OLD.deliberacao, NEW.deliberacao);
    END IF;
    IF OLD.datafinal <> NEW.datafinal THEN
        INSERT INTO log_processo (processo_id, campo_alterado, valor_antigo, valor_novo)
        VALUES (OLD.id, 'datafinal', OLD.datafinal, NEW.datafinal);
    END IF;
END//
DELIMITER ;
